"""Помощь (команда №5) — статический структурированный экран (spec §10).

Данные не нужны, реализуем полностью: цикл работы, разбор каждой команды, честные
пометки где сейчас демо-данные. Контакт для вопросов — плейсхолдер (уточнить позже).
"""

from __future__ import annotations

from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

from bot.access import OperatorOnly

router = Router(name="help")
router.message.filter(OperatorOnly())

# Плейсхолдер: заменить на реальный контакт, когда уточнит Вячеслав.
_SUPPORT_CONTACT = "@vdagency_support"

_HELP_TEXT = f"""ℹ️ <b>Как работает бот</b>

Бот ведёт клиента по циклу запуска рекламы:
1. Отправляете бриф → 2. Клиент заполняет → 3. Заводим рекламный кабинет →
4. Смотрим статистику.

<b>Команды</b>

📨 /send_brief — отправить клиенту бриф.
Выбираете тип (физлицо / ИП), указываете контакт — бот готовит приглашение со
ссылкой. Используйте в начале работы с клиентом.

📥 /pending — кто ещё не прислал бриф.
Список отправленных приглашений без ответа (старые сверху) и пришедшие за неделю.
Используйте, чтобы напомнить клиентам.

📊 /stats — статистика рекламных кабинетов.
Список кабинетов и метрики (показы, клики, расход, CTR, CPC) за период.
Сейчас показываются <b>демо-данные</b> — реальные появятся после подключения VK.

🔗 /link_userbot — подключить юзер-бота.
Авторизация аккаунта для доставки брифов в Telegram. Пока сервис доставки не
настроен, шаги работают в демо-режиме.

❓ /help — этот экран.

<b>Что сейчас на демо-данных</b>
Статистика кабинетов и доставка через юзер-бота — заглушки с пометкой «Демо».
Как только подключим VK Ads и сервис доставки, данные станут реальными сами.

<b>Вопросы</b>
Пишите: {_SUPPORT_CONTACT}"""


@router.message(Command("help"))
async def show_help(message: Message) -> None:
    """Показать экран помощи (§10)."""
    await message.answer(_HELP_TEXT, parse_mode="HTML")
